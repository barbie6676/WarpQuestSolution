package warpquest;

/**
 * Helper Class at package level to mark the coordinate of a point.
 * @author zhangmian1988@gmail.com
 */
public class Coordinate {   
    public int x;
    public int y;
    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public Coordinate() {
        this.x = 0;
        this.y = 0;
    }
    
}
